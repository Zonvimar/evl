    title VARCHAR(255) NOT NULL,
    author VARCHAR(255) NOT NULL,
    price FLOAT NOT NULL,
    year_published INT NOT NULL,
    genre VARCHAR(100) NOT NULL,
    page_count INT NOT NULL,
    publisher VARCHAR(255) NOT NULL
);

